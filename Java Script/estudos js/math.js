//funções do módulo Math:

//documentação:
//https://developer.mozilla.org/pt-BR/docs/Web/JavaScript/Reference/Global_Objects/Math

console.log(Math.random()); //gera um número aleatório entre 0 e 1

console.log(Math.max(1,2,3,4,5)); //retorna o maior valor da lista

console.log(Math.min(1,2,3,4,5)); //retorna o menor valor da lista

console.log(Math.PI); //retorna o valor de pi

console.log(Math.abs(-2)); //retorna o valor em módulo

console.log(Math.floor(2.1212)); //arredonda pra baixo

console.log(Math.ceil(2.1212)); //arredonda pra cima

console.log(Math.round(2.1212)); //arredonda pro mais próximo

console.log(Math.sqrt(9)); //retorna a raiz quadrada

console.log(Math.trunc(9.1312)); //retorna o número inteiro, ignora a parte decimal